    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            //set du lieu vao database
            modeLogin mdLG = new modeLogin();
            mdLG.setTaiKhoang(txtUse.getText());
            mdLG.setMatKhau(txtPass.getText());
            mdLG.setChucNang(cbxRole.getSelectedItem().toString());
            
            int n = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắc thêm tài khoảng này ?", "Thông báo !", JOptionPane.YES_NO_OPTION);
            if(n == JOptionPane.YES_OPTION){
                    loginDao dao = new loginDao();
                if (dao.insert(mdLG)) {
                    JOptionPane.showMessageDialog(this, "Thêm tài khoảng thành công !");
                    btnResetActionPerformed(evt);
                    loadTable();
                    setEnabldReset();
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm tài khoảng bị lỗi vui lòng kiểm tra lại !");
                }
            }
            if (n == JOptionPane.NO_OPTION) {
                return;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi ! ");
        }
    }//GEN-LAST:event_btnAddActionPerformed

