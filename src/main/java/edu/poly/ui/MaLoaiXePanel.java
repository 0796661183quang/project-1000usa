    private void btnShowLoaiXeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowLoaiXeActionPerformed
        try {
            loaiXeDao dao = new loaiXeDao();

            List<modelLoaiXe> list = dao.findByMaLoai(txtSearchMaLoaiXe.getText());
            tblmodel.setRowCount(0);

            if (list.size()>0) {
                for (modelLoaiXe mdLoaiXe : list) {

                    tblmodel.addRow(new Object[]{
                        mdLoaiXe.getMaLoaiXe(),
                        mdLoaiXe.getTenLoaiXe()

                    });
                }
                tblmodel.fireTableDataChanged(); // cập nhật toàn bộ dữ liệu bảng (chỉ dữ liệu của bảng)
            }else{
                JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi"); // show lỗi
            e.printStackTrace();

        }
    }//GEN-LAST:event_btnShowLoaiXeActionPerformed

