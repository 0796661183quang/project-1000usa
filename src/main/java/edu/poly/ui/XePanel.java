    private void btnImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImageActionPerformed
        JFileChooser fChooser = new JFileChooser();
        FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("Image File (*.jpg)", "jpg");
        fChooser.setFileFilter(imageFilter);
        fChooser.setMultiSelectionEnabled(false); //chọn 1 ảnh

        int x = fChooser.showDialog(this, "Open");
        if(x == JFileChooser.APPROVE_OPTION){
            File file = fChooser.getSelectedFile(); // cho nguoi dung chi 1 file, neu nhiu thi them "s"
            try {
                ImageIcon icon = new ImageIcon(file.getPath());
                Image img = imageHelper.resize(icon.getImage(), 450, 250);
                ImageIcon resizeIcon = new ImageIcon(img);
                lblImage.setIcon(resizeIcon);
                personalImage = imageHelper.toByteArray(img, "jpg");
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Lỗi");
            }
        }
    }//GEN-LAST:event_btnImageActionPerformed

