    private void tblSearchXeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSearchXeMouseClicked
        try {
            int row = tblSearchXe.getSelectedRow();

            if (row>=0) {
                String MaXe = (String) tblSearchXe.getValueAt(row, 0);
                xeDao dao = new xeDao();
                modelXe mdXe = dao.findByMaxeEditTxt(MaXe);

                if (mdXe != null) {
                    txtAddMaXe.setText(mdXe.getMaXe());
                    txtAddTenXe.setText(mdXe.getTenXe());
                    txtAddSoKhung.setText(mdXe.getSoKhung());
                    txtAddSoMay.setText(mdXe.getSoMay());
                    txtAddMaLuc.setText(String.format("%d" , mdXe.getMaLuc()));
                    txtAddGiaGio.setText(String.format("%d" ,mdXe.getGiaThueXeTheoGio()));
                    txtAddGiaNgay.setText(String.format("%d" ,mdXe.getGiaThueXeTheoNgay()));
                    txtAddGiaThang.setText(String.format("%d" ,mdXe.getGiaThueXeTheoThang()));
                    txtAddTienPhat.setText(String.format("%d" ,mdXe.getTienPhat()));
                    txtAddBienSoXe.setText(mdXe.getBienSoXe());
                    cbxAddHang.setSelectedItem(mdXe.getHang());
                    cbxAddMaLoaiXe.setSelectedItem(mdXe.getMaLoaiXe());
                    cbxAddSoCho.setSelectedItem(String.format("%d" , mdXe.getSoCho()));
                    cbxAddSoCho.addItem(String.format("%d", mdXe.getSoCho()));
                    cbxAddTinhTrang.setSelectedItem(mdXe.getTinhTrangXe());
                    dpkAddTimeBatDau.setDate(mdXe.getThoiGianBatDauTinhTrang());
                    dpkAddTimeKetThuc.setDate(mdXe.getThoiGianKetThucTinhTrang());
                    if(mdXe.getHinhXe() != null){
                        Image img = imageHelper.createImageFromByteArray(mdXe.getHinhXe(), "jpg");
                        lblImage.setIcon(new ImageIcon(img));
                        personalImage = mdXe.getHinhXe();
                    }else{
                        ImageIcon icon = new ImageIcon(getClass().getResource("/edu/poly/icon/icon/xe-null.jpg"));
                        lblImage.setIcon(icon);
                    }

                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi !");
        }

    }//GEN-LAST:event_tblSearchXeMouseClicked

