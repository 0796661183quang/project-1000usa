    private void btnAddLoaiXeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddLoaiXeActionPerformed
        try {
            //-------------báo lỗi-------------
            StringBuilder sb = new StringBuilder();
            
            notificationError.DataAvailable(txtAddTenLoaiXe, sb, "Trường Tên loại Xe không thể bỏ trống !");
            notificationError.DataAvailable(txtAddMaLoaiXe, sb, "Trường Mã loại Xe không thể bỏ trống !");
            
            // Hiển thị thông báo lỗi đã được đặt sẵn
            if (sb.length()>0) {
                JOptionPane.showMessageDialog(this, sb.toString());
                return;
            }
            //--------------------------------------------

            int n = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắc thêm loại xe ?", "Thông báo !", JOptionPane.YES_NO_OPTION);
            if (n == JOptionPane.YES_OPTION) {

                //set du lieu vao database
                modelLoaiXe mdLoaiXe = new modelLoaiXe();
                mdLoaiXe.setMaLoaiXe(txtAddMaLoaiXe.getText());
                mdLoaiXe.setTenLoaiXe(txtAddTenLoaiXe.getText());

                loaiXeDao dao = new loaiXeDao();
                if (dao.insert(mdLoaiXe)) {
                    JOptionPane.showMessageDialog(this, "Thêm Loại Xe thành công !");
                    btnResetActionPerformed(evt);
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm Loại Xe bị lỗi vui lòng kiểm tra lại !");
                }

            }
            loadDataToTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi !");
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddLoaiXeActionPerformed

