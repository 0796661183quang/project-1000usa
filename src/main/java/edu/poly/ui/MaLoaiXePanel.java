    private void btnDelMaLoaiXeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelMaLoaiXeActionPerformed
        try {
            StringBuilder sb = new StringBuilder();
            notificationError.DataAvailable(txtDeleteMaLoaiXe, sb, "Bạn chưa nhập thông tin");
            // Hiển thị thông báo lỗi đã được đặt sẵn
            if (sb.length()>0) {
                JOptionPane.showMessageDialog(this, sb.toString());
                return;
            }
            
            int n = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắc xóa loại xe ?", "Thông báo !", JOptionPane.YES_NO_OPTION);

            if(n == JOptionPane.YES_OPTION){
                loaiXeDao dao = new loaiXeDao();
                if (dao.delete(txtDeleteMaLoaiXe.getText())) {
                    JOptionPane.showMessageDialog(this, "Xóa Mã Loại Xe thành công !");
                    btnResetDelActionPerformed(evt);
                }else{
                    JOptionPane.showMessageDialog(this, "Xóa Mã Loại Xe thất bại. Vui lòng kiểm tra lại thông tin !");
                }
            }
            loadDataToTable();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi !");
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnDelMaLoaiXeActionPerformed

