    public void loadTable(){
        try {
            xeDao dao = new xeDao();

            List<modelXe> list = dao.showAllXe();
            model.setRowCount(0);
            
            for (modelXe mdXe : list) {
                model.addRow(new Object[]{
                    mdXe.getMaXe(),
                    mdXe.getTenXe(),
                    mdXe.getSoKhung(),
                    mdXe.getSoMay(),
                    mdXe.getMaLuc(),
                    mdXe.getHang(),
                    mdXe.getSoCho(),
                    mdXe.getGiaThueXeTheoGio(),
                    mdXe.getGiaThueXeTheoNgay(),
                    mdXe.getGiaThueXeTheoThang(),
                    mdXe.getTienPhat(),
                    mdXe.getTinhTrangXe(),
                    mdXe.getThoiGianBatDauTinhTrang(),
                    mdXe.getThoiGianKetThucTinhTrang(),
                    mdXe.getMaLoaiXe(),
                    mdXe.getBienSoXe(),
                });
                
            }
            model.fireTableDataChanged(); // cập nhật toàn bộ dữ liệu bảng

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage()); // show lỗi
        }
    }
    
