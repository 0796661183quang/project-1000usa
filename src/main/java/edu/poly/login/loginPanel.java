    public void initTable(){
        model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{
            "Tài Khoảng",
            "Mật khẩu",
            "Vai trò"
        });// đặt tiêu đề cho cột
        tblLogin.setModel(model); //set dữ liệu cho bảng
    }

    public void loadTable(){
        try {
            loginDao dao = new loginDao();

            List<modeLogin> list = dao.showLogin();
            model.setRowCount(0);
            
            list.forEach((mdLG) -> {
                model.addRow(new Object[]{
                    mdLG.getTaiKhoang(),
                    mdLG.getMatKhau(),
                    mdLG.getChucNang()
                });
            });
            model.fireTableDataChanged(); // cập nhật toàn bộ dữ liệu bảng (chỉ dữ liệu của bảng)

        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage()); // show lỗi
        }
    }
    
    private int rowSelected() {
        int row = tblLogin.getSelectedRow();
        return row;
    }
    
    private void loadCBX( JComboBox cbx) {
        String sql = "select DISTINCT chucNang from LOGIN ";
        try {
            Connection con = databaseHelper.openConnection();
            Statement sttm = con.createStatement();
            ResultSet rs = sttm.executeQuery(sql);
            while (rs.next()) {
                cbx.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
